from sklearn.naive_bayes import MultinomialNB
from sklearn.feature_extraction.text import CountVectorizer
import pandas as pd
import warnings

# Suppress SettingWithCopyWarning by importing it from the correct location (pd.errors)
warnings.filterwarnings('ignore', category=pd.errors.SettingWithCopyWarning)

# --- Data Loading and Preparation ---
df = pd.read_csv("spam.csv", encoding='latin-1')

# Select the relevant columns (v1 for label, v2 for text)
df = df.iloc[:, [0, 1]]
df.columns = ['label', 'message']

# Define the features (X) and target (y)
X = df['message']
y = df['label']

print("--- Data Loaded and Prepared ---")
print(f"Total messages: {len(X)}")
print(f"Labels: {y.unique()}")
print("------------------------------")

# --- Feature Extraction and Model Training ---

# 1. Initialize the CountVectorizer
cv = CountVectorizer()

# 2. Fit and Transform the text data (X) to create the feature matrix
X_vec = cv.fit_transform(X)

# 3. Train the Naive Bayes Model
model = MultinomialNB()
model.fit(X_vec, y)

print("✅ Model Training Complete using Multinomial Naive Bayes.")

