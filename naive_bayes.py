user_input = input("\nEnter a message to classify (e.g., 'Claim your free prize now!'):\n> ")

# Prepare the input for the model
test_message = [user_input]

# Transform the new message using the FITTED vectorizer
# This converts the user's text into the numerical format the model understands.
test_vec = cv.transform(test_message)

# Predict the label for the transformed test data
prediction = model.predict(test_vec)

